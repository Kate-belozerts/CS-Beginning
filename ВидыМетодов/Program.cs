//Вид 2: что-то принимает, но ничего не возвращает:

void Method2(string msg, int count)
{
    int i = 0;
    while (i < count)
    {
        Console.WriteLine(msg);
        i++;
    }
}
//Вызвать метод так:
Method2("text", 4); // 4 раза напишет этот текст, те первое - msg, а второе count
//Либо:
Method2(count: 4, msg: "new text");//Не важно в каком порядке переменные, если они именованные


