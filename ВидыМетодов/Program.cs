//Вид 4: что-то принимает и что-то возвращает:


string Method4(int count, string c)//char - символ
{
    int i = 0;
    string result = String.Empty;//Либо пустые кавычки: = "";

    while (i < count)
    {
        result = result + c;
        i++;
    }
    return result;
}

//Вызвать метод так:
string res = Method4(10, "z");//10 раз повторит z)
Console.WriteLine(res);

